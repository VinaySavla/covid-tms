          <h2>What happens during a blood test?</h2>
          <hr>
          <p class="lead" align="justify">A blood test will involve a blood sample being taken from a blood vessel in your arm. The arm is an ideal section of the body to take a blood sample from because it can be conveniently uncovered. The typical area for a sample to be taken is from inside the elbow or wrist, this is because the veins are relatively close to the surface. Blood samples that are taken from children are frequently taken from the back of the hand, naturally this can be a distressing experience for young children, their skin may be numbed with a special spray or cream before the sample is taken. A tight band (tourniquet) is fastened around the upper part of the upper arm. This will squeeze the arm, temporarily it will slow down the flow of blood and make the veins temporarily swell, this will make it easier for a sample to be taken. Prior to taking a sample, the doctor or nurse will sanitize the area of skin with an antiseptic wipe.</p>
          <br>
